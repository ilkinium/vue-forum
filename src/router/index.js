import Vue from 'vue'
import Router from 'vue-router'
import ThreadShow from '../pages/ThreadShowPage'
import Home from '../pages/HomePage'
import NotFound from '../pages/NotFoundPage'

Vue.use(Router)

export default new Router({
  routes: [
    {
      path: '/',
      name: 'Home',
      component: Home
    },
    {
      path: '/thread/:id',
      name: 'ThreadShow',
      component: ThreadShow,
      props: true
    },
    {
      path: '*',
      name: 'NotFound',
      component: NotFound
    }
  ],
  mode: 'history'
})
